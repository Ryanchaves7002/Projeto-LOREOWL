document.addEventListener("DOMContentLoaded", function () {
    const params = new URLSearchParams(window.location.search);
    const title = params.get('title');

    if (title && livrosTexto[title]) {
        const livro = livrosTexto[title];

        document.getElementById('bookTitle').innerText = title;

        document.querySelector(".conteudo-leitura").innerText = livro.texto;

        const bookImgElement = document.getElementById('bookImg');
        if (livro.imagem) {
            bookImgElement.src = livro.imagem;
            bookImgElement.alt = `Imagem do livro ${title}`;
        } else {
            bookImgElement.src = "https://via.placeholder.com/150";
            bookImgElement.alt = "Imagem não disponível";
        }

        const pdfButton = document.getElementById('pdfButton');
        if (livro.pdf) {
            pdfButton.style.display = 'block';
            pdfButton.onclick = function () {
                AbrirPDF(livro.pdf);
            };
        } else {
            pdfButton.style.display = 'none'; 
        }
    } else {
        document.getElementById('bookTitle').innerText = "Livro não encontrado";
        document.querySelector(".conteudo-leitura").innerText = "Texto não disponível.";
        const bookImgElement = document.getElementById('bookImg');
        bookImgElement.src = "https://via.placeholder.com/150";
        bookImgElement.alt = "Imagem não disponível";
        
        document.getElementById('pdfButton').style.display = 'none';
    }
});

function AbrirPDF(pdfUrl) {
    if (pdfUrl) {
        window.open(pdfUrl, '_blank');
    } else {
        alert("PDF não disponível para este livro.");
    }
}